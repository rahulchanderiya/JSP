package packagex;

public class complaint {

	private int complain_no;
	private String complain, feedback, activity_report;
	
	public int getComplain_no() {
		return complain_no;
	}
	public void setComplain_no(int complain_no) {
		this.complain_no = complain_no;
	}
	public String getComplain() {
		return complain;
	}
	public void setComplain(String complain) {
		this.complain = complain;
	}
	public String getFeedback() {
		return feedback;
	}
	public void setFeedback(String feedback) {
		this.feedback = feedback;
	}
	public String getActivity_report() {
		return activity_report;
	}
	public void setActivity_report(String activity_report) {
		this.activity_report = activity_report;
	}
}
